ns.Locales = {
    zhCN = L_zhCN,
    zhTW = L_zhTW,
    enUS = L_enUS
}

-- 初始化一个空的全局 L 表，所有文件都会引用这个空表
ns.L = {}

-- 动态切换语言的核心函数
function ns:SwitchLanguage(lang)
    ns.currentLang = lang
end

setmetatable(ns.L, {
    __index = function(t, k)
        -- 1. 优先使用手动指定的语言
        local lang = ns.currentLang
        
        -- 2. 如果没指定，尝试从存档读取 (支持实时读取)
        if not lang or lang == "auto" then
            lang = (ns.db and ns.db.display and ns.db.display.language) or 
                   (LDCombatStatsDB and LDCombatStatsDB.display and LDCombatStatsDB.display.language) or "auto"
        end
        
        -- 3. 如果还是 auto，则跟随客户端
        if lang == "auto" then
            local client = GetLocale()
            if client == "zhTW" then lang = "zhTW"
            elseif client == "zhCN" then lang = "zhCN"
            else lang = "enUS" end
        end
        
        local targetTable = ns.Locales[lang] or ns.Locales["zhCN"]
        return targetTable[k] or k
    end
})

-- 初始化时标记为 auto，真正获取文本时会动态判定
